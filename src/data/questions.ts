// ─── Helper functions ─────────────────────────────────────────────────────────

export const getQuestionsByDomain = (domainId: number): Question[] =>
  questions.filter((q) => q.domainId === domainId)

export const getRandomQuestions = (count: number, domainId?: number): Question[] => {
  const pool = domainId ? getQuestionsByDomain(domainId) : questions
  const shuffled = [...pool].sort(() => Math.random() - 0.5)
  return shuffled.slice(0, Math.min(count, shuffled.length))
}

export const getQuestionsByDifficulty = (difficulty: Question['difficulty']): Question[] =>
  questions.filter((q) => q.difficulty === difficulty)
